/**
 * Ключи для локального хранилища.
 * - folderURL: ключ для хранения URL папки.
 * - fileURL: ключ для хранения URL файла.
 * - request: ключ для хранения информации о запросе.
 * - requestParam: ключ для хранения параметров запроса.
 */

export enum LocalStorageKey {
	FolderURL = 'folder',
	FileURL = 'file',
	Request = 'request',
	RequestParam = 'param',
}
